"""Brand assets for the Employee ID card, embedded as base64.

Everything the ID-card PDF needs (brand fonts + logo artwork) is inlined here so
the feature works without shipping binary files. On first use the blobs are
written to a temp directory and the paths are handed to ReportLab.

Fonts are subset to the Latin characters the card uses and converted to
TrueType outlines -- ReportLab cannot embed the original CFF/OpenType .otf files.

Hindi is NOT here: the card reuses NotoSansDevanagari from services/fonts/,
which the joining kit already ships.
"""

import base64
import os
import tempfile

_DIR = None
_ASSETS = {}


def _blobs():
    return {
        "Asterone-Regular.ttf": _ASTERONE_REG,
        "Asterone-Bold.ttf": _ASTERONE_BOLD,
        "Montserrat-Bold.ttf": _MONT_BOLD,
        "Montserrat-SemiBold.ttf": _MONT_SEMI,
        "logo_dark.png": _LOGO_DARK,
        "icon_white.png": _ICON_WHITE,
        "icon_watermark.png": _ICON_WM,
    }


def asset_path(name: str) -> str:
    """Return an on-disk path for an embedded asset, materialising it on first call."""
    global _DIR
    if _DIR is None:
        _DIR = tempfile.mkdtemp(prefix="rmf_idcard_")
        for fname, b64 in _blobs().items():
            path = os.path.join(_DIR, fname)
            with open(path, "wb") as fh:
                fh.write(base64.b64decode(b64))
            _ASSETS[fname] = path
    return _ASSETS[name]


